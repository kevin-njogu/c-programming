#include <stdio.h>

int main(){
    //%d is the place holder for intergers
    printf("Today I am %d years old and next year I will be %d years old\n", 35, 36);

    //%f is the place holder for floating point number. .1f - states that we need only one decimal point
    printf("My mean grade is %.1f\n", 93.7);
    return 0;
}