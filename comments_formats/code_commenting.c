#include <stdio.h>

int main(){
    //This line prints out the name of Dummy Jones - This is a single line comment
    printf("My name is Dummy Jones\n");
    
    /*
    -This is a multiline comment
    -Anything between the /* will be treated as a comment and shall not be executed
    */
    printf("Dummy Jones is learning code commenting in c language\n");

    return 0;
}